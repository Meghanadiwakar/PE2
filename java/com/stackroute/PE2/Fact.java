package com.stackroute.PE2;

public class Fact {

        static int factCal(int x) {
            int fact = 1;
            for (int i = 1; i <= x; i++) {

                fact = fact * i;

            }
            return fact;
        }



        static long factCal1(long x) {
            int fact = 1;
            for (int i = 1; i <= x; i++) {

                fact = fact * i;

            }
            return fact;
        }
    }


